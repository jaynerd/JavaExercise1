package variable;

/**
 * This simple variable assigning exercise demonstrates how variables
 * can be assigned, and perform a print out function to the console to
 * check the stored value of the variable.
 */

public class VariableExercise {
    public static void main(String[] args) {
        int sampleValue = 53;
        System.out.println("The value of sampleValue variable is : " +
                sampleValue + ".");
    }
}
