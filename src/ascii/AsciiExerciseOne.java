package ascii;

/**
 * This simple char to int exercise demonstrates how characters can be
 * converted to int. This snippet will help students to get the concept
 * of character to ASCII value conversion (characters are represented
 * by numbers).
 */

public class AsciiExerciseOne {
    public static void main(String[] args) {
        char digit = '9'; // Char that will be converted to int.
        System.out.println(digit); // Print char to console.

        int asciiValue = digit; // Char to ascii int conversion.
        System.out.println(asciiValue); // Print corresponding int.
    }
}
